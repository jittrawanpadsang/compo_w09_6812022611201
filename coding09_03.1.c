#include <stdio.h>

int main() {
    int i = 1;             // กำหนดค่าเริ่มต้นให้ i เป็น 1

    while (i <= 10) {      // วนลูปจนกว่า i จะเกิน 10
        if (i == 6) {      // ถ้า i เป็น 6 ให้ข้ามรอบ
            i++;           // ต้องเพิ่มก่อน ไม่งั้นติดลูป
            continue;
        }

        printf("%d ", i);  // แสดงค่าของ i ถ้าไม่ใช่ 6
        i++;               // เพิ่มค่า i ทีละ 1
    }

    return 0;
}
